package BackjunMunjae.TwoWeeks;
import java.util.Scanner;
public class gizzicN {
    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);

        int num = scanner.nextInt();

        int a = num;

        for(int i=0; i<num;i++)
        {
            System.out.println(a);
            a--;
        }

    }
}
